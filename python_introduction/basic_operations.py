number1 = 10
number2 = 5

# Addition
sum = number1 + number2
print("Addition of", number1, "and", number2, "is", sum)

# Subtraction
Subtract = number1 - number2
print("Subtraction of", number1, "and", number2, "is", Subtract)


# Multiplication
multiply = number1 * number2
print("Product of", number1, "and", number2, "is:", multiply)